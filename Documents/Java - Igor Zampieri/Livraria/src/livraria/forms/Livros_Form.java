    private void jButton_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CadastrarActionPerformed
        livros li = new livros();
        livrosDAO DAO = new livrosDAO();
        
        editoras editora = (editoras) jComboBox_Editoras.getSelectedItem();
        autores autor = (autores) jComboBox_Autores.getSelectedItem();
        
        li.setEditora(editora);
        li.setAutor(autor);
        li.setTitulo(jTextField_Titulo.getText());
        li.setAno(Integer.parseInt(jTextField_Ano.getText()));
        
        DAO.cadastrar(li);
        
        carregaDados();
    }//GEN-LAST:event_jButton_CadastrarActionPerformed

