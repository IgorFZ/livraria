    public void carregaDados() {
        
        DefaultTableModel tabela = (DefaultTableModel) jTable_Titulos.getModel();
        livrosDAO dao = new livrosDAO();
        tabela.setNumRows(0);
        
        for (livros l : dao.listar()) {
            tabela.addRow(new Object[]{
                l.getId_livro(),
                l.getEditora(),
                l.getAutor(),
                l.getTitulo(),
                l.getAno()
            });    
        }
    }
    
    public void pesquisaDados() {
        
        DefaultTableModel tabela = (DefaultTableModel) jTable_Titulos.getModel();
        livrosDAO dao = new livrosDAO();
        tabela.setNumRows(0);
        
        for (livros l: dao.pesquisar((jTextField_Pesquisar.getText()))) {
            tabela.addRow(new Object[]{
                l.getId_livro(),
                l.getEditora(),
                l.getAutor(),
                l.getTitulo(),
                l.getAno()
            });    
        }
    }
    
