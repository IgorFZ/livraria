    private void jTable_TitulosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_TitulosMouseClicked
        String comboEditoras = jTable_Titulos.getValueAt(jTable_Titulos.getSelectedRow(), 1).toString();
        for (int i = 0; i < jComboBox_Editoras.getItemCount(); i++) {
            if (jComboBox_Editoras.getItemAt(i).toString().equalsIgnoreCase(comboEditoras)) {
                jComboBox_Editoras.setSelectedIndex(i);
            }
        }
        
        String comboAutores = jTable_Titulos.getValueAt(jTable_Titulos.getSelectedRow(), 2).toString();
        for (int i = 0; i < jComboBox_Autores.getItemCount(); i++) {
            if (jComboBox_Autores.getItemAt(i).toString().equalsIgnoreCase(comboAutores)) {
                jComboBox_Autores.setSelectedIndex(i);
            }
        }
        
        jTextField_Codigo.setText(jTable_Titulos.getValueAt(jTable_Titulos.getSelectedRow(), 0).toString());
        jTextField_Titulo.setText(jTable_Titulos.getValueAt(jTable_Titulos.getSelectedRow(), 3).toString());
        jTextField_Ano.setText(jTable_Titulos.getValueAt(jTable_Titulos.getSelectedRow(), 4).toString());
    }//GEN-LAST:event_jTable_TitulosMouseClicked

    private void jButton_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AlterarActionPerformed
        
        livros l = new livros();
        livrosDAO dao = new livrosDAO();

        editoras editora = (editoras) jComboBox_Editoras.getSelectedItem();
        autores autor = (autores) jComboBox_Autores.getSelectedItem();

        l.setEditora(editora);
        l.setAutor(autor);
        l.setTitulo(jTextField_Titulo.getText());
        l.setAno(Integer.parseInt(jTextField_Ano.getText()));
        l.setId_livro(Integer.parseInt(jTextField_Codigo.getText()));
        
        dao.alterar(l);
        
        carregaDados();
    }//GEN-LAST:event_jButton_AlterarActionPerformed

